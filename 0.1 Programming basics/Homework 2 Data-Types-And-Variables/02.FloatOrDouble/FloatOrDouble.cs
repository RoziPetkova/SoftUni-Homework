using System;

namespace _02.FloatOrDouble
{
    class FloatOrDouble
    {
        static void Main()
        {
            //34.567839023, 12.345, 8923.1234857, 3456.091

            double firstNum = 34.567839023;
            float secondNum = 12.345f;
            float thirdNum = 8923.1234857f;
            float lastNum = 3456.091f;

            Console.WriteLine("{0}; {1}; {2}; {3}", firstNum,secondNum,thirdNum,lastNum);

        }
    }
}
